# calculadora simple 

print("======CALCULADORA======")


num1 = float(input("Seleccione su primer numero"))
num2 = float(input("Seleccione su segundo numero"))

print (f"Suma: {num1} + {num2}")
print (f"Resta: {num1} - {num2}")
print (f"Multiplicacion: {num1} * {num2}")
print (f"Division entera: {num1} // {num2}")
print (f"Division : {num1} / {num2}")
print (f"Residuo: {num1} % {num2}")
print (f"Potencia: {num1} ** {num2}")